    def init_training_settings(self):
        self.net_g.train()
        train_opt = self.opt['train']

        self.ema_decay = train_opt.get('ema_decay', 0)
        if self.ema_decay > 0:
            logger = get_root_logger()
            logger.info(f'Use Exponential Moving Average with decay: {self.ema_decay}')
            # define network net_g with Exponential Moving Average (EMA)
            # net_g_ema is used only for testing on one GPU and saving
            # There is no need to wrap with DistributedDataParallel
            self.net_g_ema = build_network(self.opt['network_g']).to(self.device)
            # load pretrained model
            load_path = self.opt['path'].get('pretrain_network_g', None)
            if load_path is not None:
                self.load_network(self.net_g_ema, load_path, self.opt['path'].get('strict_load_g', True), 'params_ema')
            else:
                self.model_ema(0)  # copy net_g weight
            self.net_g_ema.eval()

        # define discriminator
        self.net_d = NLayerDiscriminator(n_layers=self.opt['network_d']['num_layers']).apply(weights_init)
        self.net_d = self.model_to_device(self.net_d)
        self.print_network(self.net_d)

        # load pretrained models
        load_path = self.opt['path'].get('pretrain_network_d', None)
        if load_path is not None:
            self.load_network(self.net_d, load_path, self.opt['path'].get('strict_load_d', True))

        self.net_d.train()

        # set up optimizers and schedulers
        self.setup_optimizers()
        self.setup_schedulers()

    def setup_optimizers(self):
        train_opt = self.opt['train']
        # optimizer g
        optim_params_g = []
        for k, v in self.net_g.named_parameters():
            if v.requires_grad:
                optim_params_g.append(v)
            else:
                logger = get_root_logger()
                logger.warning(f'Params {k} will not be optimized.')
        optim_type = train_opt['optim_g'].pop('type')
        self.optimizer_g = self.get_optimizer(optim_type, optim_params_g, **train_opt['optim_g'])
        self.optimizers.append(self.optimizer_g)
        # optimizer d
        optim_type = train_opt['optim_d'].pop('type')
        self.optimizer_d = self.get_optimizer(optim_type, self.net_d.parameters(), **train_opt['optim_d'])
        self.optimizers.append(self.optimizer_d)

    def optimize_parameters(self, current_iter):
        self.optimizer_g.zero_grad()
        self.output = self.net_g.module.forward_with_ccm(self.lq)

        # optimize net_g
        l_g_total = 0
        loss_dict = OrderedDict()

        # pix loss
        l_g_pix = F.mse_loss(self.output, self.gt, reduction='mean')
        l_g_total += l_g_pix
        loss_dict['l_g_pix'] = l_g_pix

        # perceptual loss
        l_g_percep = self.perceptual_loss(self.output, self.gt) * self.opt['loss']['perceptual_weight']
        l_g_total += l_g_percep
        loss_dict['l_g_percep'] = l_g_percep

        # gan loss
        fake_g_pred, _ = self.net_d(self.output)
        l_g_gan = vanilla_g_loss(fake_g_pred)
        l_g_gan = l_g_gan * calculate_adaptive_weight(l_g_pix + l_g_percep, l_g_gan, last_layer=self.net_g.module.zero_out.weight) * self.opt['loss']['disc_weight']
        l_g_total += l_g_gan
        loss_dict['l_g_gan'] = l_g_gan

        l_g_total.backward()
        self.optimizer_g.step()

        # optimize net_d
        for p in self.net_d.parameters():
            p.requires_grad = True

        self.optimizer_d.zero_grad()
        real_d_pred, fake_d_pred = self.net_d(self.gt, self.output.detach())
        l_d_gan = hinge_d_loss(real_d_pred, fake_d_pred)
        loss_dict['l_d_gan'] = l_d_gan

        l_d_gan.backward()
        self.optimizer_d.step()

        self.log_dict = self.reduce_loss_dict(loss_dict)

        if self.ema_decay > 0:
            self.model_ema(decay=self.ema_decay)

    def nondist_validation(self, dataloader, current_iter, tb_logger, save_img):
        # do not use the synthetic process during validation
        self.is_train = False
        super(RealSRTFinalModel, self).nondist_validation(dataloader, current_iter, tb_logger, save_img)
        self.is_train = True